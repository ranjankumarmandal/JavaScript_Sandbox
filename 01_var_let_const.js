// var, let, const


// var allows re-declare, initialize after declare can also be done, re-assignment value, global / function level scope, and hoisting
var name = 'Ranjan Kumar Mandal';
console.log(name);
var name = 'ranjan';
console.log(name);
name = 'kumar';
console.log(name);

// let allows initialize after declare can also be done, re-assignment value, block level scope
let name1 = 'Ranjan Kumar Mandal';
console.log(name1);
name1 = 'ranjan';
console.log(name1);

// const allows only for block level scope
const name2 = 'Ranjan Kumar Mandal';



